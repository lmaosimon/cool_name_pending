class Card

	# initialized to nil values until we figure out what to initialize to.
	def initialize
		@color = nil
		@shape = nil
		@shading = nil
		@number = nil
	end

	def getColor
		@color # implicit return
	end

	def getShape
		@shape # implicit return
	end

	def getShading
		@shading # implicit return
	end

	def getNumber
		@number # implicit return
	end

end
